#include <iostream>

using namespace std;

int main()
{

    string CharacterName = "John";
    int CharacterAge = 25;

    cout << "There is a man named " << CharacterName << endl;
    cout << "He is "<< CharacterAge << " years old" << endl;

    CharacterName = "Joe";
    CharacterAge = 30;

    cout << "He likes the name " << CharacterName << endl;
    cout << "But he does not like being " << CharacterAge << endl;

    return 0;
}
